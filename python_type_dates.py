a = None #Отсутствие данных
print(type(a))

a = 1 #Целое число
print(type(a))

a = 1.0 #Число с плавающей точкой
print(type(a))

a = 1 + 1j #Комплексное число
print(type(a))

a = '1' #Строчное значение
print(type(a)) 

a = [1, 1, 'a'] #Список
print(type(a))

a = (1, 1, 'a') #Кортеж
print(type(a))

a = {1, 2, 'a'} #Множество
print(type(a))

a = {'a':1, 'b':2} #Словарь
print(type(a))

a = True #Булевое значение может быть либо True(1) либо False(0)
print(type(a))
